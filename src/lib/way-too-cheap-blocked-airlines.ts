export const WAY_TOO_CHEAP_BLOCKED_AIRLINE_CODES: ReadonlySet<string> = new Set(BLOCKED);

export function routeHasWayTooCheapBlockedAirline(airlines: string[] | null | undefined): boolean {
  if (!airlines?.length) return false;
  for (const c of airlines) {
    const u = String(c).trim().toUpperCase();
    if (u && WAY_TOO_CHEAP_BLOCKED_AIRLINE_CODES.has(u)) return true;
  }
  return false;
}
